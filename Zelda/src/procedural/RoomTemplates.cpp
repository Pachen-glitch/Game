uint8_t openingMaskForSide(DoorSide side) {
    switch (side) {
        case DoorSide::North: return OpenNorth;
        case DoorSide::South: return OpenSouth;
        case DoorSide::East:  return OpenEast;
        case DoorSide::West:  return OpenWest;
        default: return OpenNone;
    }
}

uint8_t requiredOpeningMask(const std::vector<RoomConnection>& connections) {
    uint8_t mask = OpenNone;
    for (const auto& conn : connections) {
        mask = static_cast<uint8_t>(mask | openingMaskForSide(conn.side));
    }
    return mask;
}

const RoomTemplateDef* pickRoomTemplate(
    RoomType type,
    uint8_t requiredOpenings,
    int salt
) {
    const RoomTemplateDef* matches[16];
    int count = 0;

    for (const auto& entry : kCatalog) {
        if (entry.category != type) continue;
        if ((entry.allowedOpenings & requiredOpenings) != requiredOpenings) {
            continue;
        }
        matches[count++] = &entry;
    }

    if (count == 0) {
        for (const auto& entry : kCatalog) {
            if (entry.category != RoomType::Combat) continue;
            if ((entry.allowedOpenings & requiredOpenings) != requiredOpenings) {
                continue;
            }
            matches[count++] = &entry;
        }
    }

    if (count == 0) {
        return &kCatalog[0];
    }

    int idx = std::abs(salt) % count;
    return matches[idx];
}
